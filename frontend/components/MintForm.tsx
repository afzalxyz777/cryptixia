export default function MintForm() {
  const { address, isConnected } = useAccount();
  const { chain } = useNetwork();
  const [mounted, setMounted] = useState(false);

  // Prevent hydration mismatch (wait until client loads)
  useEffect(() => {
    setMounted(true);
  }, []);

  // Setup contract write (mint function)
  const { config } = usePrepareContractWrite({
    address: CONTRACT_ADDRESS,
    abi: CONTRACT_ABI,
    functionName: "mint",
    // value: parseEther("0.01"), // if mint costs ETH/AVAX
  });

  const { write, isLoading, isSuccess } = useContractWrite(config);

  if (!mounted) {
    // Render nothing until client-side hydration is done
    return null;
  }

  return (
    <div className="p-6 rounded-2xl shadow-lg bg-gray-900 text-white max-w-md mx-auto">
      <h2 className="text-xl font-bold mb-4">Mint Your NFT</h2>

      <p className="mb-2">
        {isConnected ? (
          <>
            Connected as <span className="font-mono">{address}</span>
            <br />
            Network:{" "}
            <span className="font-semibold">
              {chain?.name ?? "Unknown network"}
            </span>
          </>
        ) : (
          "Please connect your wallet"
        )}
      </p>

      {isConnected && (
        <button
          onClick={() => write?.()}
          disabled={!write || isLoading}
          className="px-4 py-2 rounded-lg bg-blue-600 hover:bg-blue-500 disabled:opacity-50"
        >
          {isLoading ? "Minting..." : "Mint NFT"}
        </button>
      )}

      {isSuccess && <p className="mt-3 text-green-400">âœ… Mint successful!</p>}
    </div>
  );
}

export default MintForm
